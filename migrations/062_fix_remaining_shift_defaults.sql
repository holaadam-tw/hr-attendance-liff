-- ============================================================
-- 062: 修正殘留的 09:00/18:00 固定班員工
--
-- 061 只修了 NULL 的情況，但有些員工已存 09:00/18:00（舊預設）
-- 統一改為 08:00/17:00
-- ============================================================

UPDATE employees
SET fixed_shift_start = '08:00',
    fixed_shift_end = '17:00'
WHERE shift_mode = 'fixed'
  AND (fixed_shift_start = '09:00:00' OR fixed_shift_end = '18:00:00');
